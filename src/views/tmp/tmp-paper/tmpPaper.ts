export const calcPaperPosition = (
  originLeft: number,
  originTop: number,
  delta: XYCoord,
): { left: number; top: number } => {
  return {
    left: Math.round(originLeft + delta.x),
    top: Math.round(originTop + delta.y),
  };
};

export const calcPaperPositionInside = (
  x: number,
  y: number,
  elWidth: number,
  elHeight: number,
  paperWidth: number,
  paperHeight: number,
): { x: number; y: number } => {
  return {
    x: Math.min(paperWidth - (elWidth ?? 0), Math.max(0, x)),
    y: Math.min(paperHeight - (elHeight ?? 0), Math.max(0, y)),
  };
};

export const translatePaperTemplate = (caption: string): string => {
  if (Object.keys(TmpPaperElTemplateEnum).includes(caption)) {
    let result = TmpPaperElTemplateEnum[caption];
    if (caption !== '${customMsg}') {
      result = '[变量]:' + result;
    }
    return result;
  }
  return caption;
};

